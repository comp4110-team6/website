  <p class="text text-body font-robo color-purple1">
    In order to address a shortcoming in the number of STEM graduates in the United States, it is clear we need to foster not only an interest in science, but also an appeal to children who feel that a STEM field is beyond their reach. An early obstacle to students may be their math skills, especially so as students reach middle- and high-school. By increasing math literacy early in the education process, our group hopes to increase the number of students who are passionate about STEM. We also hope we will further those students' confidence in the application of their knowledge of mathmatics in their everday lives!<br><br>
    Our game will be implemented using controls similar to existing rhythm games such as "catch the beat" or "osu!catch" in order to draw upon themes the player is already familiar with. Using Godot, an open-source game engine, allows us to develop for multiple operating systems. The team will also be leveraging Microsoft's GitHub for version control and the coordination of deliverables. Using open-source software as the backbone of our development suite allows us to carry a lower overhead than other commercial offerings; we hope to release our game free to the public!<br><br>
